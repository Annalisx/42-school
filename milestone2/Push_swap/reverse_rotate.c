#include "push_swap.h"

void	reverse_rotate(t_stack_node **stack)
{
	t_stack_node	*tail;

	if (!stack || !*stack)
		return ;
	tail = *stack;
	while (tail->next != NULL)
		tail = tail->next;
	tail->prev->next = NULL;
	tail->next = *stack;
	tail->prev = NULL;
	(*stack)->prev = tail;
	*stack = tail;
}

void	rra(t_stack_node **a)
{
	reverse_rotate(a);
	write(1, "rra\n", 4);
}

void	rrb(t_stack_node **b)
{
	reverse_rotate(b);
	write(1, "rrb\n", 4);
}

void	rrr(t_stack_node **a, t_stack_node **b)
{
	reverse_rotate(a);
	reverse_rotate(b);
	write(1, "rrr\n", 4);
}
